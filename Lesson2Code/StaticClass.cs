namespace Lesson2Code
{
    public static class StaticClass
    {
        static readonly int y;
        public static int Toplama(int x, int y)
        {
            return x + y;
        }

        public static int Çarpma(int x, int y)
        {
            return x * y;
        }

        public static int Çıkarma(int x, int y)
        {
            return x - y;
        }
    }
}
